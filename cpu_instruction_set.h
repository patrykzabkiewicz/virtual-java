#ifndef INSTRUCTION_SET_H_
#define INSTRUCTION_SET_H_

#define ADD 0x02  // addition
#define MOV 0x03  // move
#define SUB 0x04  // substraction
#define DEC 0x05  // decrementation
#define INC 0x06  // incrementation
#define RET 0x07  // return from sub-procedure
#define JMP 0x08  // unconditional jump
#define JME 0x09  // if equals jump
#define JMN 0x10  // if not equals jump


#endif // INSTRUCTION_SET_H_
